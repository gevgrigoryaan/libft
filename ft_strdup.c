#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*s;

	s = (char *)malloc(ft_strlen(s1) + 1);
	if (!s)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
